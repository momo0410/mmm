"""
主题管理器 (Theme Manager) - 系统级主题配置中心
=========================================================================

【在系统中的角色】
本模块是 new-lovely 桌面伴侣应用的主题配置核心，负责统一定义和分发前端 UI 所需的
所有颜色主题方案。模块最初由 Rust 端的 theme_manager.rs 迁移而来，当前以纯 Python
字典 + 函数的形式提供主题数据服务。

【主题数据如何流向到前端】
1. 前端通过 HTTP API 请求主题设置：
   GET /api/v1/theme/settings?current_theme=dark
2. 后端路由层将请求路由到对应的 API 处理器（routes 层）
3. API 处理器调用本模块的 get_theme_settings() 获取主题数据
4. 函数返回的 JSON 响应包含三种信息：
   - current_theme:  用户当前选择的主题标识（light / dark / sakura）
   - available_themes:  所有可用主题的 ID 列表（供前端主题切换下拉框使用）
   - theme_config:  当前主题的完整配色方案（名称、模式、全部颜色值）
5. 前端收到 JSON 后，将 theme_config.colors 中的各颜色值注入到 UI 组件的
   CSS 变量或样式系统中，实现一键切换整体配色

【配色设计原则】
- light 主题：标准的浅色办公风格，高对比度，适合日常使用
- dark 主题：护眼深色风格，低亮度背景，适合夜间/暗光环境
- sakura 主题：暖色系樱花粉风格，柔和温馨，适合偏好温暖色调的用户

【扩展方式】
要新增主题，只需在 THEMES 字典中添加新的 theme_id 条目，前端无需任何代码改动。
前端通过 available_themes 字段动态发现新主题并自动显示在切换控件中。
"""

