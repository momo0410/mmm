# =========================================================================
# get_theme_settings() — 获取用户当前主题的完整配置
# =========================================================================
# 【API 端点映射】
#   GET /api/v1/theme/settings
#
# 【功能说明】
#   根据传入的 current_theme 参数，返回该主题的完整颜色配置、当前主题标识
#   以及所有可用主题列表。这是前端初始化主题时调用的核心接口。
#
# 【参数】
#   current_theme: str — 用户当前选择的主题 ID，默认为 "light"
#                       该值通常来自前端 localStorage 或 Cookie 中保存的
#                       用户偏好，通过 URL query string 传入
#
# 【返回值结构】
#   {
#       "current_theme": str,             — 当前主题 ID，回传供前端确认
#       "available_themes": list[str],    — 所有可用主题 ID 列表，前端据此
#                                           渲染主题切换下拉菜单（light/dark/sakura）
#       "theme_config": {                 — 当前选中主题的完整配色对象
#           "name": str,
#           "mode": str,
#           "colors": {...}
#       }
#   }
#
# 【容错处理】
#   如果传入的 current_theme 在 THEMES 中不存在，默认回退到 "light" 主题，
#   确保前端始终能获取到有效的配色方案，不会因无效 theme_id 而白屏。
# =========================================================================
def get_theme_settings(current_theme: str = "light") -> Dict[str, Any]:
    """获取当前选中主题的完整设置（含所有可选主题列表与当前配色方案）"""
    return {
        "current_theme": current_theme,
        "available_themes": list(THEMES.keys()),
        "theme_config": THEMES.get(current_theme, THEMES["light"]),
    }


# =========================================================================
# get_all_themes() — 获取全部主题配置
# =========================================================================
# 【API 端点映射】
#   当前无独立 API 端点直接调用此函数。它是一个工具/辅助函数（utility），
#   可供其他服务模块或管理后台调用，用于返回 THEMES 字典的完整拷贝。
#
# 【功能说明】
#   直接返回所有已定义主题的完整配置字典，不做任何过滤或处理。
#   与 get_theme_settings() 的区别：
#   - get_theme_settings() → 返回单个主题 + 主题列表（面向前端用户）
#   - get_all_themes()     → 返回全部主题完整数据（面向内部/管理场景）
#
# 【典型用途】
#   - 管理后台页面需要一次性展示所有主题的详细配色
#   - 其他业务模块需要遍历所有主题执行批量操作（如主题预览生成）
#   - 单元测试中验证所有主题配置的完整性和一致性
# =========================================================================
def get_all_themes() -> Dict[str, Dict[str, Any]]:
    """返回所有已定义主题的完整配置字典（工具函数，供内部模块调用）"""
